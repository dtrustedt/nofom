// ─────────────────────────────────────────────────────────────
// REFERRAL LETTER GENERATOR
// ─────────────────────────────────────────────────────────────
function buildReferralLetter({ patient_name, patient_gender, age_months,
                                assessed_at, risk_level, risk_score,
                                referral, explanation, symptoms,
                                facility_name, facility_type }) {
  const ageYears  = Math.floor(age_months / 12)
  const ageMos    = age_months % 12
  const ageStr    = ageYears > 0
    ? `${ageYears} year${ageYears !== 1 ? 's' : ''} ${ageMos > 0 ? `${ageMos} month${ageMos !== 1 ? 's' : ''}` : ''}`.trim()
    : `${ageMos} month${ageMos !== 1 ? 's' : ''}`

  // Format gender for letter
  const genderStr = patient_gender && patient_gender !== 'unknown'
    ? patient_gender.charAt(0).toUpperCase() + patient_gender.slice(1)
    : 'Not recorded'

  const date = new Date(assessed_at || Date.now())
    .toLocaleDateString('en-GB', { day:'2-digit', month:'long', year:'numeric' })

  const symptomLines = SYMPTOMS
    .filter(s => symptoms?.[s.key])
    .map(s => `  • ${s.label}`)
    .join('\n') || '  • No symptoms reported'

  const facilityStr = facility_name
    ? `${facility_name}${facility_type ? ` (${FACILITY_TYPE_LABEL[facility_type]})` : ''}`
    : 'Not recorded'

  return `PAEDIATRIC CANCER TRIAGE REFERRAL LETTER
Generated by Nofom Triage Platform
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

Date of Assessment: ${date}

PATIENT DETAILS
Patient Name : ${patient_name || 'Not provided'}
Age          : ${ageStr}
Gender       : ${genderStr}

ASSESSING FACILITY
Facility        : ${facilityStr}

TRIAGE RESULT
Risk Level   : ${risk_level}
Risk Score   : ${risk_score}
Assessed by  : Nofom Rule-Based Triage Engine v1

REFERRAL RECOMMENDATION
Action       : ${referral?.label || '—'}
Timeframe    : ${referral?.timeframe || '—'}
Facility Tier: ${referral?.facility_tier || '—'}

SYMPTOMS REPORTED
${symptomLines}

${referral?.special_notes?.length > 0
  ? `CLINICAL WARNINGS\n${referral.special_notes.map(n => `  ⚠ ${n}`).join('\n')}\n`
  : ''}
CLINICAL REASONING
${explanation?.map((e, i) => `${i + 1}. ${e}`).join('\n\n') || '—'}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
CLINICAL NOTICE: This letter was generated by a rule-based
triage assistant. It does not constitute a diagnosis. All
referral decisions must be confirmed by a qualified health
professional. Please attach clinical notes at the receiving
facility.
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━`
}

// ─────────────────────────────────────────────────────────────
// REFERRAL LETTER DRAWER
// ─────────────────────────────────────────────────────────────
function ReferralLetterDrawer({ result, onClose }) {
  const [copied, setCopied] = useState(false)
  const letter = buildReferralLetter(result)

  const handleCopy = async () => {
    await navigator.clipboard.writeText(letter)
    setCopied(true)
    setTimeout(() => setCopied(false), 2500)
  }

  const handlePrint = () => {
    const w = window.open('', '_blank')
    w.document.write(`<html><head><title>Nofom Referral Letter</title>
      <style>
        body { font-family: 'Courier New', monospace; padding: 40px;
               font-size: 13px; line-height: 1.6; color: #000; }
        pre  { white-space: pre-wrap; word-wrap: break-word; }
        @media print { body { padding: 20px; } }
      </style></head><body><pre>${letter}</pre></body></html>`)
    w.document.close()
    w.print()
  }

  return (
    <div style={{
      position: 'fixed', inset: 0, zIndex: 100,
      background: 'rgba(10,61,107,0.55)',
      display: 'flex', flexDirection: 'column',
      justifyContent: 'flex-end'
    }}
      onClick={e => { if (e.target === e.currentTarget) onClose() }}
    >
      <div style={{
        background: 'var(--color-surface)',
        borderRadius: '16px 16px 0 0',
        maxHeight: '85dvh',
        display: 'flex',
        flexDirection: 'column',
        overflow: 'hidden'
      }}>
        {/* Drawer handle */}
        <div style={{ display:'flex', alignItems:'center', justifyContent:'space-between',
                      padding:'16px 16px 12px', borderBottom:'1px solid var(--color-border)',
                      flexShrink:0 }}>
          <div style={{ display:'flex', alignItems:'center', gap:8 }}>
            <FileText size={16} style={{ color:'var(--color-primary)' }} />
            <span style={{ fontWeight:700, fontSize:'0.9375rem',
                           color:'var(--color-text-primary)' }}>
              Referral Letter
            </span>
          </div>
          <button onClick={onClose} style={{
            background:'none', border:'none', cursor:'pointer',
            color:'var(--color-text-muted)', fontSize:'1.25rem',
            lineHeight:1, padding:'2px 6px', fontFamily:'var(--font-sans)'
          }}>×</button>
        </div>

        {/* Letter content */}
        <div style={{ flex:1, overflowY:'auto', padding:'16px' }}>
          <pre style={{
            fontFamily: '"IBM Plex Mono", "Courier New", monospace',
            fontSize: '0.75rem',
            lineHeight: 1.7,
            color: 'var(--color-text-primary)',
            background: 'var(--color-surface-alt)',
            borderRadius: 'var(--radius-md)',
            padding: '16px',
            whiteSpace: 'pre-wrap',
            wordBreak: 'break-word',
            margin: 0
          }}>
            {letter}
          </pre>
        </div>

        {/* Actions */}
        <div style={{
          display:'flex', gap:10, padding:'12px 16px 20px',
          borderTop:'1px solid var(--color-border)', flexShrink:0
        }}>
          <button onClick={handleCopy}
            className="nf-btn nf-btn-secondary" style={{ flex:1 }}>
            {copied ? <Check size={15} /> : <Copy size={15} />}
            {copied ? 'Copied!' : 'Copy text'}
          </button>
          <button onClick={handlePrint}
            className="nf-btn nf-btn-primary" style={{ flex:1 }}>
            <FileText size={15} />
            Print / Save PDF
          </button>
        </div>
      </div>
    </div>
  )
}


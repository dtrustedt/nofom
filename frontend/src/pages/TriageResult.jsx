// ─────────────────────────────────────────────────────────────
// MAIN PAGE
// ─────────────────────────────────────────────────────────────
const TOTAL = 4

export default function TriageResult() {
  const navigate         = useNavigate()
  const lastTriageResult = useAppStore(s => s.lastTriageResult)
  const viewportRef      = useRef(null)
  const [current, setCurrent]     = useState(0)
  const [showLetter, setShowLetter] = useState(false)

  useEffect(() => {
    if (!lastTriageResult) navigate('/')
  }, [lastTriageResult])

  if (!lastTriageResult) return null

  const { referral } = lastTriageResult

  // ── Programmatic scroll ─────────────────────────────────
  const goTo = useCallback((i) => {
    const el = viewportRef.current
    if (!el) return
    el.scrollTo({ left: i * el.offsetWidth, behavior: 'smooth' })
    setCurrent(i)
  }, [])

  const goNext = useCallback(() => goTo(Math.min(current + 1, TOTAL - 1)), [current, goTo])
  const goPrev = useCallback(() => goTo(Math.max(current - 1, 0)),         [current, goTo])

  const handleScroll = useCallback(() => {
    const el = viewportRef.current
    if (!el) return
    const i = Math.round(el.scrollLeft / el.offsetWidth)
    if (i !== current) setCurrent(i)
  }, [current])

  return (
    <div style={{
      height: '100dvh',
      display: 'flex',
      flexDirection: 'column',
      background: 'var(--color-surface-alt)',
      fontFamily: 'var(--font-sans)',
      overflow: 'hidden'
    }}>
      <Header />

      {/* Progress bar */}
      <div style={{ height:3, background:'var(--color-border)', flexShrink:0, position:'relative' }}>
        <div style={{
          position:'absolute', top:0, left:0, height:'100%',
          background:'var(--color-primary)',
          width:`${((current + 1) / TOTAL) * 100}%`,
          transition:'width 0.35s ease',
          borderRadius:'0 999px 999px 0'
        }}/>
      </div>

      {/* ── SWIPE VIEWPORT ── */}
      <div
        ref={viewportRef}
        onScroll={handleScroll}
        style={{
          flex: 1,
          display: 'flex',
          overflowX: 'scroll',
          overflowY: 'hidden',
          scrollSnapType: 'x mandatory',
          WebkitOverflowScrolling: 'touch',
          scrollbarWidth: 'none',
          msOverflowStyle: 'none',
        }}
      >
        {[
          <Slide1 key="s1" result={lastTriageResult} goNext={goNext} />,
          <Slide2 key="s2" referral={referral} goNext={goNext} goPrev={goPrev}
                  onShowLetter={() => setShowLetter(true)} />,
          <Slide3 key="s3" referral={referral} goNext={goNext} goPrev={goPrev} />,
          <Slide4 key="s4" result={lastTriageResult} goPrev={goPrev}
                  onDashboard={() => navigate('/')}
                  onNewAssessment={() => navigate('/triage/new')} />
        ].map((slide, i) => (
          <div key={i} style={{
            flex: '0 0 100%',
            width: '100%',
            maxWidth: 520,
            height: '100%',
            overflowY: 'hidden',
            scrollSnapAlign: 'start',
            display: 'flex',
            flexDirection: 'column',
            background: 'var(--color-surface)',
            borderRight: '1px solid var(--color-border)'
          }}>
            {slide}
          </div>
        ))}
      </div>

      <Dots current={current} total={TOTAL} goTo={goTo} />

      {/* Referral letter drawer */}
      {showLetter && (
        <ReferralLetterDrawer
          result={lastTriageResult}
          onClose={() => setShowLetter(false)}
        />
      )}
    </div>
  )
}
